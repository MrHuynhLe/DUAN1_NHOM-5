    private void loadTable() {
        ArrayList<model.SanPham> list = SPservice.GetAllSanPham();

        modelSP.setRowCount(0);
        for (model.SanPham sp : list) {
            modelSP.addRow(new Object[]{
                sp.getMaSP(),
                sp.getTenSP(),
                sp.getSoLuong(),
                sp.getGia(),
                sp.getMaccc(),
                sp.getThuonghieu(),
                sp.getXuatxu(),
                sp.getKhoiluong(),
                sp.getTrangThai()
            });
        }
    }

    private void loadTableTimKiem(List<model.SanPham> listTimSP) {

        modelSP.setRowCount(0);
        for (model.SanPham sp : listTimSP) {
            modelSP.addRow(new Object[]{
                sp.getMaSP(),
                sp.getTenSP(),
                sp.getSoLuong(),
                sp.getGia(),
                sp.getMaccc(),
                sp.getThuonghieu(),
                sp.getXuatxu(),
                sp.getKhoiluong(),
                sp.getTrangThai()
            });
        }

    }

    private void ShowDetail() {
        int index = tbl_ChiTietSanPham.getSelectedRow();

        txtMaSP.setText(tbl_ChiTietSanPham.getValueAt(index, 0).toString());

        txtTenSP.setText(tbl_ChiTietSanPham.getValueAt(index, 1).toString());

        txt_SoLuong.setText(tbl_ChiTietSanPham.getValueAt(index, 2).toString());

        txtGia.setText(tbl_ChiTietSanPham.getValueAt(index, 3).toString());
        
        txtNCC.setText(tbl_ChiTietSanPham.getValueAt(index, 4).toString());

        ThuongHieu th = (ThuongHieu) tbl_ChiTietSanPham.getValueAt(index, 5);
        ModelComboTH.setSelectedItem(th);

        XuatXu xx = (XuatXu) tbl_ChiTietSanPham.getValueAt(index, 6);
        ModelComboXX.setSelectedItem(xx);

        KhoiLuong kl = (KhoiLuong) tbl_ChiTietSanPham.getValueAt(index, 7);
        ModelComboKL.setSelectedItem(kl);

    }

    private boolean checkTrungMaSP(String MaSP) {
        for (int i = 0; i < tbl_ChiTietSanPham.getRowCount() - 1; i++) {
            if (tbl_ChiTietSanPham.getValueAt(i, 0).toString().equalsIgnoreCase(MaSP)) {
                return true;
            }
        }
        return false;
    }

    private model.SanPham getSanPhamFromInput() {

        model.SanPham sp = new model.SanPham();
        sp.setMaSP(txtMaSP.getText());
        sp.setTenSP(txtTenSP.getText());
        sp.setSoLuong(Integer.parseInt(txt_SoLuong.getText()));
        sp.setGia(Double.valueOf(txtGia.getText()));
        sp.setMaccc(txtNCC.getText());
        sp.setThuonghieu((ThuongHieu) ModelComboTH.getSelectedItem());
        sp.setXuatxu((XuatXu) ModelComboXX.getSelectedItem());
        sp.setKhoiluong((KhoiLuong) ModelComboKL.getSelectedItem());

        return sp;

    }

