    private void tbnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnThemActionPerformed
        // TODO add your handling code here:
        try {
            //check form
            if (txtMaSP.getText().equalsIgnoreCase("")
                    || txtTenSP.getText().equalsIgnoreCase("")
                    || txtGia.getText().equalsIgnoreCase("")
                    || txt_SoLuong.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
                return;
            }
            if (checkTrungMaSP(txtMaSP.getText())) {
                JOptionPane.showMessageDialog(this, "Mã SP đã có rồi, không thêm được");
                return;
            }
            //check giá, sô lượng là số , và lớn hơn 0
            try {
                double gia = Double.parseDouble(txtGia.getText());
                int soLuong = Integer.parseInt(txt_SoLuong.getText());
                if (gia < 0) {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập lại giá tiền");
                    return;
                }
                if (soLuong < 0) {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập lại số lượng san phẩm");
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số cho giá tiền hoặc số lượng ");
                return;
            }

            //Lấy dữ liệu trên form
            model.SanPham sp = getSanPhamFromInput();
            if (SPservice.addSanPham(sp) != null) {
                JOptionPane.showMessageDialog(this, "thêm thành công");
                loadTable();
            } else {
                JOptionPane.showMessageDialog(this, "Không thêm được dòng nào");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm");
        }
    }//GEN-LAST:event_tbnThemActionPerformed

