    private void ShowDetail() {
        int index = tblKhachHang.getSelectedRow();
        txtMaKH.setText(tblKhachHang.getValueAt(index, 0).toString());
        txtTenKH.setText(tblKhachHang.getValueAt(index, 1).toString());
        rdoNam.setSelected(tblKhachHang.getValueAt(index, 2).toString().equalsIgnoreCase("Nam") ? true : false);
        rdoNu.setSelected(!tblKhachHang.getValueAt(index, 2).toString().equalsIgnoreCase("Nam") ? true : false);
        txtDiaChi.setText(tblKhachHang.getValueAt(index, 3).toString());
        txtSDT.setText(tblKhachHang.getValueAt(index, 4).toString());
        cboLoai.setSelectedItem(tblKhachHang.getValueAt(index, 5));
        txtTongChiTieu.setText(tblKhachHang.getValueAt(index, 6).toString());
    }

    private boolean checkTrungMaKH(String MaKh) {
        for (int i = 0; i < tblKhachHang.getRowCount() - 1; i++) {
            if (tblKhachHang.getValueAt(i, 0).toString().equalsIgnoreCase(MaKh)) {
                return true;
            }
        }
        return false;
    }

    private model.KhachHang getForm() {
        model.KhachHang kh = new model.KhachHang();
        kh.setMaKH(txtMaKH.getText());
        kh.setTenKH(txtTenKH.getText());
        kh.setGioitinh(rdoNam.getText().toString().equalsIgnoreCase("Nam") ? "Nam" : "Nữ");

        kh.setDiaChi(txtDiaChi.getText());
        kh.setSDT(txtSDT.getText());
        kh.setLoai(cboLoai.getSelectedItem().equals("Khách thường") ? "Khách thường" : "Khách VIP");
        kh.setChiTieu(Double.parseDouble(txtTongChiTieu.getText()));
        return kh;
    }

    private void clearForm() {
        txtDiaChi.setText("");
        txtMaKH.setText("");
        txtSDT.setText("");
        txtTenKH.setText("");
        txtTongChiTieu.setText("");

    }

    private void loadTableTimKiem(List<model.KhachHang> timkiem) {
        model.setRowCount(0);
        for (model.KhachHang kh : timkiem) {
            model.addRow(new Object[]{
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getGioitinh().equalsIgnoreCase("Nam") ? "Nam" : "Nữ",
                kh.getDiaChi(),
                kh.getSDT(),
                kh.getLoai(),
                kh.getChiTieu()
               
            });
        }
    }
