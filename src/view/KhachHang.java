    private void Loadtable() {
        ArrayList<model.KhachHang> list = KHservice.getAllKhachHang();
        model.setRowCount(0);
        for (model.KhachHang kh : list) {
            model.addRow(new Object[]{
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getGioitinh().equalsIgnoreCase("Nam") ? "Nam" : "Nữ",
                kh.getDiaChi(),
                kh.getSDT(),
                kh.getLoai(),
                kh.getChiTieu()
                
            }
            );

        }
    }

