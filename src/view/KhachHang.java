    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            int chon = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa?");
            if (chon != JOptionPane.YES_OPTION) {
                return;
            }
            String MaKH = txtMaKH.getText();
            if (KHservice.XoaKH(MaKH) != null) {
                JOptionPane.showMessageDialog(this, "xóa thành công");
                Loadtable();
                if (tblKhachHang.getRowCount() > 0) {
                    tblKhachHang.setRowSelectionInterval(0, 0);
                    ShowDetail();
                } else {
                    clearForm();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không xóa được dòng nào");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi xóa");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

